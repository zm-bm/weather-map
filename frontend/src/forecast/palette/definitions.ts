function palette(
  id: string,
  label: string,
  valueUnit: string,
  stops: readonly PaletteColorStop[],
): FieldPaletteDefinition {
  return {
    id,
    label,
    valueUnit,
    outOfRange: 'clamp',
    boundaryMode: 'lower-bound-inclusive',
    stops,
  }
}

